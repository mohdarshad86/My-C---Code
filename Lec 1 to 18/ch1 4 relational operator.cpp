#include<iostream>
using namespace std;

//RELATIONAL OPERATOR

int main(){
    
    int a = 2;
    int b = 3;
    
    bool first = (a==b);
    cout<<first<<endl;
    
    bool second = (a<b);
    cout<<second<<endl;
    
    bool third = (a>b);
    cout<<third<<endl;
    
    bool fo = (a<=b);
    cout<<fo<<endl;
    
    bool fi = (a>=b);
    cout<<fi<<endl;
    
    bool six = (a!=b);
    cout<<six<<endl;
    return 0;
}